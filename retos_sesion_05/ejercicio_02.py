# Escribe un programa en Python que convierta las siguientes temperaturas de grados Celsius a grados Fahrenheit:
# 30 ºC
# -273.99 ºC
# 100 ºC
A = 30 
fahrenheit = A * 9 / 5 + 32 
print ("30 ºC a grados Fahrenheit es: ")
print (fahrenheit) 

B = -273.99 
fahrenheit = B * 9 / 5 + 32 
print ("-273.99 ºC a grados Fahrenheit es: ")
print (fahrenheit) 

C = 100 
fahrenheit = C * 9 / 5 + 32 
print ("100 ºC a grados Fahrenheit es: ")
